package projeto.jfc;

import java.awt.event.ActionEvent;

import javax.swing.AbstractAction;

import projeto.jfc.JCadastroFuncionarioPanel;

public class CadastroFuncionarioCancelarAction extends AbstractAction {
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	/**
	 * 
	 */
	private CadastroFuncionarioPanel panel;

	/**
	 * 
	 * @param panel
	 */
	public CadastroFuncionarioCancelarAction(CadastroFuncionarioPanel panel) {
		super("Cancelar");
		this.panel = panel;
	}

	@Override
	public void actionPerformed(ActionEvent arg0) {
		panel.clear();
	}

}